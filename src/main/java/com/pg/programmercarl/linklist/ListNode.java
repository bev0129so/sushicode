package com.pg.programmercarl.linklist;

/**
 * @author luojx
 * @date 2024/3/5 14:23
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
